public class dataType{
	public static void main (String[] args) {
		// data type = 숫자, 글자 등등등...
		System.out.println(6); // Number
		// System.out.println 쉽게 만드는 방법 : sout ctrl+space
		System.out.println("six"); // String
		System.out.println("6"); // String
		
		System.out.println(6+6); // 12 (Number)
		System.out.println("6"+"6"); // 66 (String)
		
		System.out.println(6*6); // 36 (Number)
		//System.out.println("6"*"6"); 문자열은 곱할 수 없다.
		
		System.out.println("111111".length()); // 6 (Number)
	}
}